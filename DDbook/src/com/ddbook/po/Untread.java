package com.ddbook.po;

public class Untread {
	public int untreadid;
	public int cid;
	public int sellerid;
	public String orderid;
	public double total;
	public int state;
	public int getUntreadid() {
		return untreadid;
	}
	public void setUntreadid(int untreadid) {
		this.untreadid = untreadid;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public int getSellerid() {
		return sellerid;
	}
	public void setSellerid(int sellerid) {
		this.sellerid = sellerid;
	}
	public String getOrderid() {
		return orderid;
	}
	public void setOrderid(String orderid) {
		this.orderid = orderid;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
}
