package com.ddbook.po;

public class UntreadItem {
	public int untreaditemid;
	public int untreadid;
	public int bid;
	public int number;
	public double subtotal;
	public int state;
	public int getUntreaditemid() {
		return untreaditemid;
	}
	public void setUntreaditemid(int untreaditemid) {
		this.untreaditemid = untreaditemid;
	}
	public int getUntreadid() {
		return untreadid;
	}
	public void setUntreadid(int untreadid) {
		this.untreadid = untreadid;
	}
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public double getSubtotal() {
		return subtotal;
	}
	public void setSubtotal(double subtotal) {
		this.subtotal = subtotal;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
}
